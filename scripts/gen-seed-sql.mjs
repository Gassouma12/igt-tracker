// Generates supabase/seed.sql from the migrated seed JSON. Columns are quoted
// camelCase to match the schema. Run: node scripts/gen-seed-sql.mjs
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const seed = (f) => JSON.parse(readFileSync(join(root, 'src/data/seed', f), 'utf8'))

// table name -> seed file, in FK-safe insert order.
// Only the org structure (LCs + users) is seeded — no sales/pipeline data.
const TABLES = [
  ['local_committees', 'localCommittees.json'],
  ['users', 'users.json'],
]

function val(v) {
  if (v === null || v === undefined) return 'NULL'
  if (typeof v === 'number') return String(v)
  if (typeof v === 'boolean') return v ? 'true' : 'false'
  if (Array.isArray(v)) return `'{${v.map((x) => `"${String(x).replace(/"/g, '\\"')}"`).join(',')}}'`
  return `'${String(v).replace(/'/g, "''")}'`
}

let out = `-- AUTO-GENERATED by scripts/gen-seed-sql.mjs — run in the Supabase SQL editor.
-- Idempotent: re-running skips rows that already exist (on conflict do nothing).
begin;
`

for (const [table, file] of TABLES) {
  const rows = seed(file)
  if (!rows.length) continue
  out += `\n-- ${table} (${rows.length})\n`
  for (const row of rows) {
    const cols = Object.keys(row)
    const colSql = cols.map((c) => `"${c}"`).join(', ')
    const valSql = cols.map((c) => val(row[c])).join(', ')
    out += `insert into ${table} (${colSql}) values (${valSql}) on conflict (id) do nothing;\n`
  }
}

out += `\ncommit;\n`
writeFileSync(join(root, 'supabase/seed.sql'), out)
console.log(`Wrote supabase/seed.sql (${out.split('\n').length} lines)`)
